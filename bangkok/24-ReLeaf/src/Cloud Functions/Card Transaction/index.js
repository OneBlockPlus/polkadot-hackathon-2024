const functions = require("@google-cloud/functions-framework");
const crypto = require("crypto");
const Firestore = require("@google-cloud/firestore");
const ethers = require("ethers");

const privateKey = `-----BEGIN RSA PRIVATE KEY-----
-----END RSA PRIVATE KEY-----`;

const provider = new ethers.providers.JsonRpcProvider(
  "https://rpc.api.moonbeam.network/"
);

const cloudPrivateKey =
  "0x";

const wallet = new ethers.Wallet(cloudPrivateKey, provider);

const db = new Firestore({
  projectId: "releaf-xxxxxx",
  keyFilename: "credential.json",
});

const Accounts = db.collection("Accounts");

functions.http("helloHttp", async (req, res) => {
  try {
    const decrypted = decryptText(req.body.data);
    const toAddress = req.body.to;
    const amount = req.body.amount;
    const tokenAddress = req.body.tokenAddress;
    const query = await Accounts.where(
      "cardHash",
      "==",
      decrypted.toString()
    ).get();
    if (query.empty) {
      throw "Query Empty";
    } else {
      if(tokenAddress ==="0x0000000000000000000000000000000000000000"){
        const pubKey = query.docs[0].data().publicKey;
        const aaContract = new ethers.Contract(pubKey, abiAA_ABI, provider);    
        let transaction = await aaContract.populateTransaction.withdrawDepositTo(toAddress, ethers.utils.parseEther(amount))
        const createReceipt = await wallet.sendTransaction(transaction);
        res.send(`${createReceipt.hash}`);
      }
      else{
        const pubKey = query.docs[0].data().publicKey;
                console.log({
          tokenAddress,
          toAddress,
          pubKey
        })
        const aaContract = new ethers.Contract(pubKey, abiAA_ABI, provider);   
        const tokenContract = new ethers.Contract(
          tokenAddress,
          abiERC20,
          provider,
        );
        const tokenDecimals = await tokenContract.decimals();
        const amountToken = ethers.utils.parseUnits(amount, tokenDecimals);
        
        const data = tokenContract.interface.encodeFunctionData('transfer', [
          toAddress,
          amountToken,
        ]);
        const dataExecute = aaContract.interface.encodeFunctionData('execute', [
          tokenAddress,
          0,
          data,
        ]);
        const transaction = {
          to: pubKey,
          data:dataExecute,
        };
        const createReceipt = await wallet.sendTransaction(transaction);
        res.send(`${createReceipt.hash}`);
      }      
    }
  } catch (e) {
    console.log(e)
    res.send(`Bad Request`);
  }
});

function decryptText(encryptedText) {
  return crypto.privateDecrypt(
    {
      key: privateKey,
    },
    Buffer.from(encryptedText, "base64")
  );
}

